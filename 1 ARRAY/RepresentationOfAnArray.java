public class RepresentationOfAnArray {
    void demoArray(){
        int[] ages = new int[3];     //declaration of an array
        ages[0]=22;
        ages[1]=23;
        ages[2]=233;
        System.out.println(ages[0]);
        System.out.println(ages[1]);
        System.out.println(ages[2]);
    }
    public static void main(String[] args) {
        RepresentationOfAnArray obj = new RepresentationOfAnArray();
        obj.demoArray();
    }
}
